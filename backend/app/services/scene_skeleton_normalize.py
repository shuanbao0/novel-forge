def normalize_action_kind(value: str | None) -> str:
    """LLM 输出 → 规范动作枚举. 未命中保留原值(供上层日志诊断)."""
    if not isinstance(value, str):
        return ""
    s = value.strip()
    if not s:
        return ""
    # 完全相等优先
    if s in _ACTION_SYNONYMS:
        return _ACTION_SYNONYMS[s]
    # 长串子串匹配: 长的优先 ("内心独白"先于"独白")
    for syn in sorted(_ACTION_SYNONYMS.keys(), key=len, reverse=True):
        if syn in s:
            return _ACTION_SYNONYMS[syn]
    return s  # 保留原值, 让 _compute_hot 仍能跨章累计同一种原始输出


def normalize_emotion_beat(value: str | None) -> str:
    """LLM 输出 → 规范情绪枚举. 未命中保留原值."""
    if not isinstance(value, str):
        return ""
    s = value.strip()
    if not s:
        return ""
    if s in _EMOTION_SYNONYMS:
        return _EMOTION_SYNONYMS[s]
    for syn in sorted(_EMOTION_SYNONYMS.keys(), key=len, reverse=True):
        if syn in s:
            return _EMOTION_SYNONYMS[syn]
    return s


def normalize_role_pair_key(value: str | None) -> str:
    """规范化 role_pair_key: 拆开两个角色名 → 字典序排列 → 重新拼接.

    LLM 经常输出 "陈建国↔林川" 与 "林川↔陈建国" 不一致, 字面比对当成不同对.
    本函数强制字典序, 让两种写法等价.
    """
    if not isinstance(value, str):
        return ""
    s = value.strip()
    if not s:
        return ""
    for sep in ("↔", "<->", "<>", "&", "/", "、"):
        if sep in s:
            parts = [p.strip() for p in s.split(sep) if p.strip()]
            if len(parts) == 2:
                a, b = sorted(parts)
                return f"{a}↔{b}"
            break
    return s
